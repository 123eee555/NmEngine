package me.nimnon.nmengine.util;

public class Rect {

	public double x = 0d;
	public double y = 0d;
	public double width = 1d;
	public double height = 1d;
	
	public Rect() {
		
	}
	
	public Rect(double x, double y, double width, double height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

}
